document.querySelectorAll('.category-link').forEach(link => {
  link.addEventListener('click', (event) => {
    event.preventDefault();
    const category = event.target.dataset.category;
    if (category === 'attractions') {
      window.location.href = 'lublin_gallery.html'; // Przekierowanie do lublin_gallery.html
    } else {
      updateCategoryContent(category);
    }
  });
});

function updateCategoryContent(category) {
  const categoryData = categories[category];
  document.getElementById('gallery-title').innerText = categoryData.title;
  document.getElementById('gallery-text').innerText = categoryData.text;

  const galleryImage = document.getElementById('gallery-image');
  galleryImage.classList.remove('show');

  setTimeout(() => {
    galleryImage.src = categoryData.images[0];
    setTimeout(() => galleryImage.classList.add('show'), 100);
  }, 500);

  let currentImageIndex = 0;

  document.getElementById('prevArea').onclick = function () {
    if (currentImageIndex > 0) {
      currentImageIndex--;
    } else {
      currentImageIndex = categoryData.images.length - 1;
    }
    galleryImage.classList.remove('show');
    setTimeout(() => {
      galleryImage.src = categoryData.images[currentImageIndex];
      setTimeout(() => galleryImage.classList.add('show'), 100);
    }, 500);
  };

  document.getElementById('nextArea').onclick = function () {
    if (currentImageIndex < categoryData.images.length - 1) {
      currentImageIndex++;
    } else {
      currentImageIndex = 0;
    }
    galleryImage.classList.remove('show');
    setTimeout(() => {
      galleryImage.src = categoryData.images[currentImageIndex];
      setTimeout(() => galleryImage.classList.add('show'), 100);
    }, 500);
  };
}

window.addEventListener('load', () => {
  const hash = window.location.hash.substring(1);
  if (hash) {
    updateCategoryContent(hash);
  }
});
